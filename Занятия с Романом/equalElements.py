#2. Функция equal_elements([2,2,2,2]) -> True, возвращает истину если
#все элементы массива одинаковы, ложь,
#если хотя бы один элемент не равен остальным

def equal_elements(a):
    for i in range(len(a)):
        if a.count(a[i]) == len(a):
            a = True
            return a
        else:
            a = False
            return a
