#3. Функция merge([2,5,9],[1,4,8]) -> [1,2,4,5,8,9], сливающая два
#отсортированных массива так,
#чтобы результриующий массив был отсортирован.


def merge(a,b):
    c = []
    c = a+b
    c.sort()
    return c
